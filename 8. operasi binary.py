a = 38
b = 2



c = a | b
print("nilai :", a ,"binary", format(a,"08b"))
print("nilai :", b ,"binary", format(b,"08b"))
print("di OR kan menjadi")
print("nilai :", c ,"binary", format(c,"08b"))

#note 08b = binary 8 angka
print("\n")
c = a & b
print("nilai :", a ,"binary", format(a,"08b"))
print("nilai :", b ,"binary", format(b,"08b"))
print("di AND kan menjadi")
print("nilai :", c ,"binary", format(c,"08b"))


print("\n")
c = a ^ b
print("nilai :", a ,"binary", format(a,"08b"))
print("nilai :", b ,"binary", format(b,"08b"))
print("di XOR kan menjadi")
print("nilai :", c ,"binary", format(c,"08b"))


