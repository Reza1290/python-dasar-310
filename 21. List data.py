# jenis list

# list int

list_int = [1,2,3,4,5]
print(list_int)

list_str = ["ucup","budi","iban","rudi"]
print(list_str)

list_bool = [True,False,True,False]
print(list_bool)

list_campuran = ["asu",1,True,"udah",False,4]
print(list_campuran)

list_range = list(range(0,10))       #start,stop,step
print(list_range)

list_pakai_for = [i for i in range(0,10,2)] # range dalam dengan loop
print(list_pakai_for)

list_pakai_for = [i*2 for i in range(0,10)] #operator di listnya
print(list_pakai_for)

list_pakai_for = [i for i in range(0,10) if i%2 !=0] #bisa pakai if
print(list_pakai_for)




