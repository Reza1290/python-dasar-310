# if else statement kek c++ but more simple
# use == ( boolean ) jangan lupa

nama = input("Masukkan Nama = ")

# single action
if nama=="badut":
    print("selamat datang Badut kamu diterima disini")
print(f"terima kasih {nama}")

# double action true or false

if nama=="badut":
    print(f"selamat kamu diterima {nama}")
    print("cek link ini")
else:
    print(f"mohon maaf kamu tidak diterima {nama}")
print(f"terima kasih {nama}")

